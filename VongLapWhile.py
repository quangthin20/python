''' Vòng lặp while trong python
cấu trúc: 
while <condition>:
    block
'''
'''
# Ví dụ: In ra 10 số tự nhiên đầu tiên
i = 0
while (i < 5):
    print(i)
    i += 1
'''
# lệnh break
'''Cú pháp:
while <condition>:
    <block1>
    break
Ý nghĩa: kết thúc vòng lặp ngay trong nó, các vòng lặp ngoài không ảnh hưởng
'''
#Lệnh continue
'''Cú pháp:
while <condition>:
    <block1>
    continue
    <block2>
    ....
    <blockn>
#Ý nghĩa: Khi thực hiện xong lệnh 1 thì nó bỏ qua tất các còn lại để tiếp tục vòng lặp
'''
